#include <unistd.h>

char	*ft_strcat(char *dest, char *arc)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (dest[i] != '\0')
		i++;
	while (arc[j] != '\0')
	{
		if (arc[j] != '\0')
		{
			dest[i] = arc[j];
			i++;
			j++;
		}
	}
	dest[i] = '\0';
	return (dest);
}